// you cannot start with a number
// examples:-
// 1value (invalid)
// value1 (valid)

var value1 = 10;
console.log(value1);
console.log(value1 + 10);
console.log(value1 - 10);
console.log(value1 * 10);
console.log(value1 ** 3);

// you can use only underscore _ or dollar symbol
// first_name (valid)
// _firstname (valid)
 var first_name = 10;
 console.log(first_name);

 // first$name (valid)
 // $firstname (valid)
 var first$name = 50;
 console.log(first$name);

